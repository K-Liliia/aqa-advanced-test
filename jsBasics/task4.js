// task 4.1

let radius = 3;
let areaOfCircle;

areaOfCircle = Math.PI * radius * radius;
console.log("Area of circle with radius " + radius + " is " + areaOfCircle.toFixed(2) + " square centimeters");

// task 4.2

let rectangleLength = 9;
let rectangleWidth = 5;
let areaOfRectangle = (rectangleLength * rectangleWidth).toFixed(2);

console.log("Area of rectangle is " + areaOfRectangle + " square centimeters");

// task 4.3

let cylinderRadius = 4;
let cylinderHeight = 7;
let areaOfCylinder;

areaOfCylinder = (Math.PI * cylinderRadius * cylinderRadius * cylinderHeight).toFixed(2);
console.log("Area of cylinder is " + areaOfCylinder + " square centimeters");